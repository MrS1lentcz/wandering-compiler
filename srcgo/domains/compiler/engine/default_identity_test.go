// TestPlan_DefaultIdentityAdd_OverPriorLiteralDefault — prev has a
// literal default that must be dropped before ADD GENERATED. Down
// must restore the prior literal alongside DROP IDENTITY.
func TestPlan_DefaultIdentityAdd_OverPriorLiteralDefault(t *testing.T) {
	cls := testClassifier(t)
	prev := mkIdentityBaseSchema(&irpb.Default{Variant: &irpb.Default_LiteralInt{LiteralInt: 100}})
	curr := mkIdentityBaseSchema(&irpb.Default{Variant: &irpb.Default_Auto{Auto: irpb.AutoKind_AUTO_IDENTITY}})

	probe, _ := engine.Plan(prev, curr, cls, nil, pgOnlyEmitter)
	res := []*planpb.Resolution{{
		FindingId: probe.Findings[0].GetId(),
		Strategy:  planpb.Strategy_NEEDS_CONFIRM,
		Actor:     "test",
	}}
	plan, err := engine.Plan(prev, curr, cls, res, pgOnlyEmitter)
	if err != nil {
		t.Fatalf("Plan: %v", err)
	}
	up := plan.Migrations[0].GetUpSql()
	for _, frag := range []string{
		"ALTER TABLE accounts ALTER COLUMN id DROP DEFAULT;",
		"ALTER TABLE accounts ALTER COLUMN id ADD GENERATED BY DEFAULT AS IDENTITY;",
	} {
		if !strings.Contains(up, frag) {
			t.Errorf("up missing %q\n---\n%s", frag, up)
		}
	}
	// Order check on the post-BEGIN body so the risk header comments
	// (which mention "ADD GENERATED" in rationale text) don't confuse
	// strings.Index.
	body := up
	if i := strings.Index(body, "BEGIN;"); i >= 0 {
		body = body[i:]
	}
	dropIdx := strings.Index(body, "DROP DEFAULT")
	addIdx := strings.Index(body, "ADD GENERATED")
	if dropIdx < 0 || addIdx < 0 || dropIdx > addIdx {
		t.Errorf("DROP DEFAULT must precede ADD GENERATED in body (PG rejects mixed); body:\n%s", body)
	}
	down := plan.Migrations[0].GetDownSql()
	for _, frag := range []string{
		"ALTER TABLE accounts ALTER COLUMN id DROP IDENTITY;",
		"ALTER TABLE accounts ALTER COLUMN id SET DEFAULT 100;",
	} {
		if !strings.Contains(down, frag) {
			t.Errorf("down missing %q\n---\n%s", frag, down)
		}
	}
}

