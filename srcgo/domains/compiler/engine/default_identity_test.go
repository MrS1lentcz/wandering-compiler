// TestPlan_AutoKindChange_InAxis — AUTO_NOW → literal on DATETIME.
// In-axis SAFE (no Finding); emitter renders SET DEFAULT with the
// sem-type-correct autoExpr.
func TestPlan_AutoKindChange_InAxis(t *testing.T) {
	cls := testClassifier(t)
	mk := func(def *irpb.Default) *irpb.Schema {
		return &irpb.Schema{Tables: []*irpb.Table{{
			Name: "events", MessageFqn: "tg.Event",
			Columns: []*irpb.Column{
				{Name: "id", ProtoName: "id", FieldNumber: 1,
					Carrier: irpb.Carrier_CARRIER_INT64, Type: irpb.SemType_SEM_ID,
					DbType: irpb.DbType_DBT_BIGINT, Pk: true},
				{Name: "recorded_at", ProtoName: "recorded_at", FieldNumber: 2,
					Carrier: irpb.Carrier_CARRIER_TIMESTAMP, Type: irpb.SemType_SEM_DATETIME,
					Default: def},
			},
			PrimaryKey: []string{"id"},
		}}}
	}
	prev := mk(&irpb.Default{Variant: &irpb.Default_Auto{Auto: irpb.AutoKind_AUTO_NOW}})
	curr := mk(&irpb.Default{Variant: &irpb.Default_LiteralString{LiteralString: "1970-01-01 00:00:00"}})

	plan, err := engine.Plan(prev, curr, cls, nil, pgOnlyEmitter)
	if err != nil {
		t.Fatalf("Plan: %v", err)
	}
	if len(plan.Findings) != 0 {
		t.Fatalf("auto_kind_change should be in-axis SAFE, got findings: %v", plan.Findings)
	}
	up := plan.Migrations[0].GetUpSql()
	if !strings.Contains(up, "ALTER TABLE events ALTER COLUMN recorded_at SET DEFAULT '1970-01-01 00:00:00';") {
		t.Errorf("up missing literal SET DEFAULT:\n%s", up)
	}
	down := plan.Migrations[0].GetDownSql()
	if !strings.Contains(down, "ALTER TABLE events ALTER COLUMN recorded_at SET DEFAULT NOW();") {
		t.Errorf("down should restore NOW() (sem-type-driven):\n%s", down)
	}
}
