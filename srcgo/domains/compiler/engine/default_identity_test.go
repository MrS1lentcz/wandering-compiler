package engine_test

// D38 integration tests — engine.Plan on a default identity
// transition, exercising:
//   (1) identity_add (prev nil → curr AUTO_IDENTITY) → AlterColumn
//       with DefaultChange; emit renders ADD GENERATED + setval.
//   (2) identity_drop (prev AUTO_IDENTITY → curr nil) → AlterColumn;
//       emit renders DROP IDENTITY + down recreates via setval.
//   (3) Wrong strategy on identity_add → hard-error.
//   (4) auto_kind_change (AUTO_NOW → literal) — in-axis FactChange,
//       no Finding, emitter handles sem-type via real column.

import (
	"strings"
	"testing"

	"github.com/MrS1lentcz/wandering-compiler/srcgo/domains/compiler/engine"
	irpb "github.com/MrS1lentcz/wandering-compiler/srcgo/pb/domains/compiler/types/ir"
	planpb "github.com/MrS1lentcz/wandering-compiler/srcgo/pb/domains/compiler/types/plan"
)

func mkIdentityBaseSchema(def *irpb.Default) *irpb.Schema {
	col := &irpb.Column{
		Name: "id", ProtoName: "id", FieldNumber: 1,
		Carrier: irpb.Carrier_CARRIER_INT64,
		Type:    irpb.SemType_SEM_ID,
		DbType:  irpb.DbType_DBT_BIGINT,
		Pk:      true,
		Default: def,
	}
	return &irpb.Schema{Tables: []*irpb.Table{{
		Name: "accounts", MessageFqn: "shop.Account",
		Columns: []*irpb.Column{
			col,
			{Name: "email", ProtoName: "email", FieldNumber: 2,
				Carrier: irpb.Carrier_CARRIER_STRING, Type: irpb.SemType_SEM_TEXT},
		},
		PrimaryKey: []string{"id"},
	}}}
}

func TestPlan_DefaultIdentityAdd_NeedsConfirm(t *testing.T) {
	cls := testClassifier(t)
	prev := mkIdentityBaseSchema(nil)
	curr := mkIdentityBaseSchema(&irpb.Default{Variant: &irpb.Default_Auto{Auto: irpb.AutoKind_AUTO_IDENTITY}})

	probe, err := engine.Plan(prev, curr, cls, nil, pgOnlyEmitter)
	if err != nil {
		t.Fatalf("probe: %v", err)
	}
	if len(probe.Findings) != 1 {
		t.Fatalf("want 1 finding, got %d", len(probe.Findings))
	}
	f := probe.Findings[0]
	if f.GetAxis() != "default_identity_add" {
		t.Fatalf("want axis default_identity_add, got %s", f.GetAxis())
	}
	if f.GetProposed() != planpb.Strategy_NEEDS_CONFIRM {
		t.Fatalf("want Proposed=NEEDS_CONFIRM, got %s", f.GetProposed())
	}

	res := []*planpb.Resolution{{
		FindingId: f.GetId(),
		Strategy:  planpb.Strategy_NEEDS_CONFIRM,
		Actor:     "test",
	}}
	plan, err := engine.Plan(prev, curr, cls, res, pgOnlyEmitter)
	if err != nil {
		t.Fatalf("Plan: %v", err)
	}
	up := plan.Migrations[0].GetUpSql()
	for _, frag := range []string{
		"ALTER TABLE accounts ALTER COLUMN id ADD GENERATED BY DEFAULT AS IDENTITY;",
		"SELECT setval(pg_get_serial_sequence('accounts', 'id'), COALESCE((SELECT MAX(id) FROM accounts), 1), true);",
	} {
		if !strings.Contains(up, frag) {
			t.Errorf("up missing fragment %q\n---\n%s", frag, up)
		}
	}
	down := plan.Migrations[0].GetDownSql()
	if !strings.Contains(down, "ALTER TABLE accounts ALTER COLUMN id DROP IDENTITY;") {
		t.Errorf("down missing DROP IDENTITY:\n%s", down)
	}
}

